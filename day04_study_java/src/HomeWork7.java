public class HomeWork7 {
    public static void main(String[] args) {
        /*
        打印数字三角形2
        */
        int i = 1;
        do{
           for(int j = 1;j<=i;j++){
               System.out.print(i);
           }
           i++;
            System.out.println();
        }while(i<=5);
    }
}
