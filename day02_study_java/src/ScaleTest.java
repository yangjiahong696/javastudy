public class ScaleTest {
    public static void main(String[] args) {
        /*
        十进制 = 1010101010
        二进制 = 682
        八进制 = 136348168
        十六进制 = 269488144
         */
        System.out.println("十进制 = " + 1010101010);
        System.out.println("二进制 = " + 0B1010101010);
        System.out.println("八进制 = " + 01010101010);
        System.out.println("十六进制 = " + 0X10101010);
    }
}
